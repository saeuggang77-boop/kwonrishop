/**
 * 게시글에 대한 전체 대화 스레드 생성 프롬프트
 */
export function getConversationThreadPrompt(
  authorPersonality: GhostPersonality,
  authorName: string,
  postTitle: string,
  postContent: string,
  commenterNames: string[],
  commenterPersonalities: GhostPersonality[],
  threadSize: number,
  authorReplyRate: number = 50
): string {
  const authorPersonalityDesc = PERSONALITY_PROMPTS[authorPersonality]
    .split('\n')[0]
    .replace('성격: ', '');

  const commenterList = commenterNames
    .map((name, i) => {
      const personalityDesc = PERSONALITY_PROMPTS[commenterPersonalities[i]]
        .split('\n')[0]
        .replace('성격: ', '');
      return `- ${name}: ${personalityDesc}`;
    })
    .join('\n');

  return `${COMMENT_SYSTEM_PROMPT}

다음 게시글에 대한 자연스러운 댓글 대화 스레드를 생성합니다.

게시글 작성자: ${authorName} (${authorPersonalityDesc})
게시글 제목: ${postTitle}
게시글 내용: ${postContent}

댓글 참여자:
${commenterList}

🔥 핵심 대화 규칙 (반드시 지킬 것):

0. 작성자(${authorName})는 절대 최상위 댓글(replyTo: null)을 작성하지 않습니다!
   - 작성자는 반드시 다른 사람의 댓글에 대한 답글(replyTo: 숫자)만 작성
   - 첫 번째 메시지는 반드시 댓글 참여자 중 한 명이 써야 함 (글쓴이가 본인 글에 댓글 다는 건 부자연스러움)

1. 메시지 길이 다양화 (매우 중요!):
   - 30%는 짧은 리액션 (5-20자): "ㅋㅋ 진짜요?", "헐 대박이네요", "공감이에요ㅠ"
   - 40%는 중간 대화 (30-80자): "아 저도 그런 적 있는데요 진짜 짜증나더라고요ㅋㅋ"
   - 30%는 긴 자기 경험 공유 (100-200자): 2~3줄로 자기 이야기 풀기

2. 티키타카 패턴 (실제 커뮤니티처럼):
   - 댓글자A가 공감 → 글쓴이(${authorName})가 "ㅎㅎ 감사해요~" 짧게 → 댓글자B가 자기 경험 공유
   - 글쓴이가 "오 진짜요?? 저도 그런 적 있어요" → 댓글자A가 "맞아요 그거 공감이에요"
   - 같은 사람이 연속 2번 답글하는 것도 자연스러움 (추가 반응)

3. 주고받는 대화 패턴 (40~60% 확률로 적용):
   - A가 댓글 → 글쓴이 답글 → A가 다시 댓글(추가 질문/감사) → 글쓴이 다시 답글
   - 이런 주고받기가 실제 커뮤니티에서 가장 자연스러운 패턴
   - 예: A "그거 어디서 사셨어요?" → 글쓴이 "쿠팡에서요~" → A "아 거기!! 감사해요 ㅎㅎ" → 글쓴이 "ㅎㅎ 네~"
   - 핵심: 같은 사람이 글쓴이와 2~3번 주고받는 것이 자연스러움

4. 대화 흐름:
   - 글쓴이(${authorName})는 전체 대화의 약 ${authorReplyRate}%를 차지하도록 답글 (자연스럽게 소통하는 글쓴이)
   - 답글 안 하는 댓글도 있어야 자연스러움 (그냥 넘어가는 것)
   - 댓글자끼리 대화하는 것도 자연스러움 (30~40%)
   - 중간에 화제가 살짝 바뀌어도 OK
   - 답글 없는 댓글이 1~2개는 꼭 있어야 함

5. 실제 커뮤니티 스타일 예시:
   짧은 리액션: "ㅋㅋㅋ 진짜요", "헐 대박이네요", "공감이에요ㅠ", "아 그거ㅋㅋ", "맞아요 ㅎㅎ"
   중간 공감: "아 저도 그런 적 있는데요 진짜 짜증나더라고요ㅋㅋ 힘내세요!"
   긴 자기 경험: "저도 얼마 전에 비슷한 일 있었는데요\n그때 중개사한테 얘기했더니 오히려 제 편 들어주셔서\n좀 나아졌어요ㅎㅎ 한번 말해보세요!"

6. 자연스러운 대화 요소:
   - "아 맞다", "근데", "ㅋㅋㅋㅋ", "헐", "오", "진짜??" 같은 리액션
   - 줄바꿈(\n) 사용: 긴 메시지는 2~3줄로
   - 같은 길이로만 쓰지 말 것 (다양하게!)

7. 누가 봐도 봇이 아닌 진짜 사람들이 대화하는 것처럼 작성

총 ${threadSize}개의 메시지를 생성해주세요.

🚫🚫🚫 반드시 존댓말만 사용하세요. 반말 사용 시 결과물은 폐기됩니다.
문장 끝은 반드시 "~요", "~네요", "~어요", "~습니다", "~인가요?" 등 존댓말로 끝내야 합니다.

반드시 아래 JSON 배열 형식으로만 반환하세요:
[
  {"name": "댓글자이름", "content": "댓글내용", "replyTo": null},
  {"name": "${authorName}", "content": "답글내용", "replyTo": 0},
  ...
]

replyTo: null이면 게시글에 대한 댓글, 숫자면 해당 인덱스의 메시지에 대한 답글
name: 반드시 위에 명시된 이름(${[authorName, ...commenterNames].join(', ')}) 중 하나만 사용`;
}

